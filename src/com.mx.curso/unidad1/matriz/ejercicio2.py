# Creacion de matriz 3x3

matriz = [
    [0,1,2],
    [3,4,5],
    [6,7,8]
          ]

# Imprimir los elementos
# Imprimir la matriz completa

print(matriz[0][0]) # 0
print(matriz[0][1]) # 1
print(matriz[0][2]) # 2

print(matriz[1][0]) # 3
print(matriz[1][1]) # 4
print(matriz[1][2]) # 5

print(matriz[2][0]) # 6
print(matriz[2][1]) # 7
print(matriz[2][2]) # 8

# Modificar el valor de un elemento de la matriz

matriz[2][0] = 188
print(matriz[2][0])

